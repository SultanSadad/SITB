<script>
    document.addEventListener('DOMContentLoaded', function() {
        const form = document.getElementById('addPasienForm');

        form.addEventListener('submit', function(e) {
            e.preventDefault();

            const formData = new FormData(form);

            fetch('{{ url("/rekammedis/pasien/store") }}', {
                    method: 'POST',
                    body: formData,
                    headers: {
                        'X-CSRF-TOKEN': document.querySelector('input[name="_token"]').value
                    }
                })
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        // Tutup modal
                        document.querySelector('[data-modal-hide="authentication-modal"]').click();
                        // Reload halaman untuk refresh data
                        window.location.href = '{{ url("/dashboardrekammedis") }}';
                    } else {
                        console.error('Gagal simpan:', data);
                    }
                })
                .catch(error => {
                    console.error('Error:', error);
                });
        });
    });
</script>

